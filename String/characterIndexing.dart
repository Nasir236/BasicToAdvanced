void main() {
  // Character Indexing Finding
  var str = "Hello World";
  var charIndex = str[3];
  print("Character Index 3 is: $charIndex");

  //Example Second index first character
  var str2 = "Dartworld";
  var charaterSpecific = str2[0];
  print("This is First Index Character: $charaterSpecific");

  var str3 = "English";
  var lastChar = str3[str3.length - 1];
  print("This is Last Index Character: $lastChar");
}
